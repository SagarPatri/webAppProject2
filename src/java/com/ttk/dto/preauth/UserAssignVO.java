package com.ttk.dto.preauth;

public class UserAssignVO {
	
	private String contactSeqId="";
	private String contactName="";
	private String userAssignflag="";
	
	public String getContactSeqId() {
		return contactSeqId;
	}
	public void setContactSeqId(String contactSeqId) {
		this.contactSeqId = contactSeqId;
	}
	public String getContactName() {
		return contactName;
	}
	public void setContactName(String contactName) {
		this.contactName = contactName;
	}
	public String getUserAssignflag() {
		return userAssignflag;
	}
	public void setUserAssignflag(String userAssignflag) {
		this.userAssignflag = userAssignflag;
	}
	
	

}
